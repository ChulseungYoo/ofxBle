#include "ship.h"

//------------------------------------------------------------------------------
Ship::Ship()
{
}

//------------------------------------------------------------------------------
Ship::Ship(ofPoint* ptPos)
{
    _position = ptPos;
}

//------------------------------------------------------------------------------
void Ship::setPos(ofPoint* ptPos)
{
    _position = ptPos;
}

//------------------------------------------------------------------------------
float Ship::getY()
{
    return _position->y;
}

//------------------------------------------------------------------------------
float Ship::getX()
{
    return _position->x;
}

//------------------------------------------------------------------------------
void Ship::draw()
{
    ofImage::draw(_position->x, _position->y);
}

//------------------------------------------------------------------------------
void Ship::update() {
    _position->x = _position->x + _movement->x;
    _position->y = _position->y + _movement->y;
}

//------------------------------------------------------------------------------
void Ship::setMovement(ofPoint* ptMovement) {
    _movement = ptMovement;
}

//------------------------------------------------------------------------------
Ship::~Ship()
{
    //dtor
}
